
public class Pojo {
	private String id;
	private SomeCode code;
	
	
	public Pojo(String id, SomeCode code) {
		super();
		this.id = id;
		this.code = code;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public SomeCode getCode() {
		return code;
	}

	public void setCode(SomeCode code) {
		this.code = code;
	}
	
}
